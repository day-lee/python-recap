"""
[실무형 기본 문제: 장바구니 상품 금액 계산기]

장바구니에 담긴 상품들의 (상품명, 가격) 튜플들을 입력받아서 
총 결제 금액을 계산하는 함수를 작성하세요. 
이때 회원 등급에 따라 추가 할인율을 적용할 수 있어야 합니다.

요구사항:
1. `*args`로 (상품명, 가격) 형태의 튜플들을 여러 개 받으세요.
2. `discount_rate`라는 옵셔널 인자(기본값 0.0)를 지정하세요.
3. 반복문(for)을 사용해 튜플에서 '가격'만 꺼내어 총합을 구하세요.
4. 최종 총합에서 할인율을 적용한 [최종 금액]을 반환하세요.
   - 공식: 총합 * (1 - discount_rate)
"""

def calculate_total(*args, discount_rate=0.0):
    total_price = 0
    #for문 안에서 언패킹 가능함 
    for _, price in args:
        total_price += price 
    return total_price * (1 - discount_rate)
    # 1. args에 들어있는 튜플들을 순회하며 가격을 total_price에 더하세요.
    # (힌트: 튜플 언패킹을 쓰면 편리합니다.)
    
    # 2. discount_rate를 적용한 최종 금액을 반환하세요.


# ==============================================================================
# [테스트 실행 코드]
# ==============================================================================
if __name__ == "__main__":
    item1 = ("키보드", 50000)
    item2 = ("마우스", 30000)

    # 1. 할인율 없이 기본 계산할 때
    print(calculate_total(item1, item2))  
    # 예상 출력: 80000.0 (또는 80000)

    # 2. 10% 할인율(0.1)을 적용하여 계산할 때
    print(calculate_total(item1, item2, discount_rate=0.1))  
    # 예상 출력: 72000.0
